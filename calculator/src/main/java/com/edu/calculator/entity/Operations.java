package com.edu.calculator.entity;

public class Operations {

	//sum
	public static int sum(int a, int b) 
	{
		return (a+b);
	}
	
	// substraction
	public static int sub(int a, int b) 
	{
		return (a-b);
	}
	
	// Multiplication
	public static int mul(int a, int b) 
	{
		return (a*b);
	}
	
	//division
	public static int div(int a, int b) 
	{
		return (a/b);
	}
	
}
